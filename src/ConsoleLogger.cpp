int ConsoleLogger::log(char* text)
{
    int ret = -1;
    do {
        if (text == NULL) {
            break;
        }
        std::lock_guard<std::mutex> lock(this->log_Locker);
        std::cout << "\n[CONSOLE LOGGING]: ";
        std::cout << text << "\n";
        ret = 0;
    } while (0);
    return ret;
}
