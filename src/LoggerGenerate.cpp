Logger* generate(log_type_t type)
{
    Logger* logger = NULL;
    switch (type)
    {
    case LOG_TYPE_CONSOLE:
        logger = new ConsoleLogger();
        break;
    case LOG_TYPE_FILE:
        logger = new FileLogger();
        break;
    default:
        break;
    }
    return logger;
}

void destroy(Logger** logger)
{
    do
    {
        if ((logger == NULL)
            || (*logger == NULL))
        {
            break;
        }
        delete* logger;
        *logger = NULL;
    } while (0);
}
